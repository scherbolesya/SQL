result = {}
text = input()
w = []
for word in text.split():
    w.append(word.lower().strip('.,;:-?!'))
s1 = set(i for i in w)
for i in s1:
    key = w.count(i)
    result[key] = result.get(key, []) + [i]
print(sorted(result[min(result.keys())])[0])

