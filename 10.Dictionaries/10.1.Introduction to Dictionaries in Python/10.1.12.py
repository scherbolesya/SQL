# Какие значения связаны с ключами в соответствующих словарях?
# What values are associated with the keys in the corresponding dictionaries?

int_dict = {2: 'two', 5: 'five', 7: 'seven', 9: 'nine'}
str_dict = {'three': 3, 'seven': 7, 'nine': 9}

int_dict[2] --- two
int_dict[7] --- seven
str_dict['seven'] --- 7
str_dict['nine'] --- 9
